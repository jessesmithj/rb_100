def crunch(string)
  crunch_array = []
  string.chars.each do |char|
    crunch_array << char unless crunch_array.last == char
  end
  crunch_array.join
end

puts crunch('ddaaiillyy ddoouubbllee') == 'daily double'
puts crunch('4444abcabccba') == '4abcabcba'
puts crunch('ggggggggggggggg') == 'g'
puts crunch('a') == 'a'
puts crunch('') == ''


